package lezione00_intro;
	
public class TipiDato {
	public static void main(String[] args) {
		// TIPI DI DATO PRIMITIVI
		//numerici e interi
		byte eta = 29;
		byte scarpe = 2;
		byte numStudenti = 127;
		
		short litriPiscina = 30000; //16bit =2^16
		
		int sooldiInBanca = 2000000000; //32bit
		
		long etaTerra = 4000000000L; //64bit
		
		//numeri con la virgola
		
		float piGreco = 3.14f; //32bit
		double nepero = 2.71; //64bit
		
		//char, tipo valore carattere. Ogni lettera corrisponde ad un numero
		char lettera = 'A'; //16bit
		
		//boolean 1 bit
		boolean vero = true; //1
		boolean falso = false; //0
		
	}
}
